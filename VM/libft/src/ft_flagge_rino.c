#include "libft.h"

void	ft_flagge_rino(t_rem *rems, char c, char b)
{
	if (c == '+')
		rems->fg[0] = '+';
	if (c == '-')
		rems->fg[1] = '-';
	if (c == '.')
		rems->fg[2] = '.';
	if (c == '#')
		rems->fg[3] = '#';
	if (c == ' ')
		rems->fg[4] = ' ';
	if (c == '0' && (b < '0' || b > '9'))
		rems->fg[5] = '0';
}
