#include "libft.h"

char	*ft_zero_scp(char **p_r)
{
	char	*temp;
	int		cn;

	cn = 0;
	while (p_r[0][cn] == '0' && p_r[0][cn + 1] != '.' && p_r[0][cn + 1] != '\0'
			&& p_r[0][cn] != '\0')
		cn++;
	temp = ft_strdup(&p_r[0][cn]);
	free(*p_r);
	return (temp);
}
