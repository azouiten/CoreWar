#include "libft.h"

static void	test(int n, char c)
{
	if (n >= 10 || n <= -10)
	{
		test(n / 10, c);
		test(n % 10, c);
	}
	else
	{
		if (n >= 0)
		{
			c = n + '0';
			write(1, &c, 1);
		}
		else
		{
			c = (-1 * n) + '0';
			write(1, &c, 1);
		}
	}
}

void		ft_putnbr(int n)
{
	char c;

	c = 0;
	if (n < 0)
		write(1, "-", 1);
	test(n, c);
}
